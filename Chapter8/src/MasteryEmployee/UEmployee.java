
package MasteryEmployee;

public class UEmployee 
{
	
	double salary;
	String name;
	
	public UEmployee(double s, String n)
	{
		salary = s;
		name = n;
	}

    public String getName() 
    {
        return name;
    }

    public double getSalary() 
    {
        return salary;
    }
    
}
